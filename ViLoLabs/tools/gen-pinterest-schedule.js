#!/usr/bin/env node
/* ─────────────────────────────────────────────────────────────────────
   Generate the 28-day Pinterest schedule from the 4 approved rules.
   Reads grade→themes from sheets.html, produces:
     Resources/pinterest-28-day-schedule.md   (human-reviewable table)
   ───────────────────────────────────────────────────────────────────── */
const fs = require('fs');
const path = require('path');

// ── Rule 1: weekday → grade ─────────────────────────────────────────
// JS getDay(): Sun=0, Mon=1, Tue=2, Wed=3, Thu=4, Fri=5, Sat=6
const WEEKDAY_TO_GRADE = {
  1:'prekg', 2:'kg', 3:'1', 4:'2', 5:'3', 6:'4', 0:'5'
};
const GRADE_LABEL = {
  prekg:'Pre-KG', kg:'KG', '1':'Class 1', '2':'Class 2',
  '3':'Class 3', '4':'Class 4', '5':'Class 5'
};

// ── Themes available per grade (mirrors sheets.html LEVEL_TOPICS) ───
const LEVEL_TOPICS = {
  prekg:['animals','fruits','insects','nature','toys','clothing','fantasy'],
  kg:   ['animals','fruits','vegetables','insects','nature','vehicles','toys','clothing','body-parts','birds','sea-creatures'],
  '1':  ['animals','fruits','vegetables','vehicles','food','insects','instruments','nature','stationery','birds','sea-creatures','people','plants','dinosaurs','household'],
  '2':  ['animals','food','vehicles','insects','instruments','nature','stationery','sports','plants','dinosaurs','household','tools','buildings','people'],
  '3':  ['animals','food','vehicles','nature','stationery','sports','symbols','tools','buildings','people','household','sea-creatures'],
  '4':  ['food','vehicles','nature','stationery','sports','symbols','tools','buildings','plants','dinosaurs','household'],
  '5':  ['vehicles','nature','stationery','sports','symbols','tools','buildings','plants','people','food']
};

// Pretty theme labels for headlines
const THEME_LABEL = {
  animals:'Animals', fruits:'Fruits', vegetables:'Vegetables',
  vehicles:'Vehicles', food:'Food', insects:'Insects',
  instruments:'Instruments', nature:'Nature', stationery:'Stationery',
  birds:'Birds', 'sea-creatures':'Sea Creatures', people:'People',
  plants:'Plants', dinosaurs:'Dinosaurs', household:'Household',
  toys:'Toys', clothing:'Clothing', fantasy:'Fantasy',
  'body-parts':'Body Parts', sports:'Sports', symbols:'Symbols',
  tools:'Tools', buildings:'Buildings'
};

// ── Rule 3: activity preset by weekday ──────────────────────────────
function activityFor(weekday) {
  if (weekday === 0) return 'colouring';  // Sunday → colouring
  return 'mix';                            // everything else → mix
}

// ── Rule 2: theme rotation within grade ─────────────────────────────
// Alphabetical order, advance by weekIndex (mod), avoid back-to-back
// same-theme across the SITE-WIDE day stream.
function pickTheme(grade, weekIndex, prevDayTheme) {
  const pool = [...LEVEL_TOPICS[grade]].sort(); // alphabetical
  let idx = weekIndex % pool.length;
  // Skip if it matches yesterday's theme site-wide
  for (let tries = 0; tries < pool.length; tries++) {
    if (pool[idx] !== prevDayTheme) return pool[idx];
    idx = (idx + 1) % pool.length;
  }
  return pool[idx]; // fallback
}

// ── Build the 28 rows ───────────────────────────────────────────────
const START = new Date('2026-06-13T00:00:00'); // Sat 13 Jun 2026
const rows = [];
const weekCounter = {}; // per grade, how many times we've seen this grade

let prevTheme = '';
for (let d = 0; d < 28; d++) {
  const date = new Date(START); date.setDate(START.getDate() + d);
  const wd = date.getDay();
  const grade = WEEKDAY_TO_GRADE[wd];
  weekCounter[grade] = (weekCounter[grade] || 0);
  const theme = pickTheme(grade, weekCounter[grade], prevTheme);
  weekCounter[grade]++;
  prevTheme = theme;

  const activity = activityFor(wd);
  const headline = `${GRADE_LABEL[grade]} ${THEME_LABEL[theme]} Worksheet`;
  // Slug placeholder — real ID is generated by the bot via WSeo.newSeed()
  // when the pin is created. For the review doc, show the slug shape.
  const slugShape = `${grade}-${theme}-${activity}-<id>`;

  const dayName = date.toLocaleDateString('en-GB',{weekday:'short'});
  const dateStr = date.toLocaleDateString('en-GB',{day:'2-digit',month:'short'});

  rows.push({
    n: d+1, dayName, dateStr, grade: GRADE_LABEL[grade], theme: THEME_LABEL[theme],
    activity, headline, slugShape, gradeKey:grade, themeKey:theme
  });
}

// ── Render as a Markdown table ──────────────────────────────────────
const lines = [];
lines.push(`# ViLoLabs · Pinterest 28-Day Schedule`);
lines.push(``);
lines.push(`Generated: ${new Date().toISOString().slice(0,10)} · Starts: Sat 13 Jun 2026 · 28 daily pins`);
lines.push(``);
lines.push(`## How this was generated`);
lines.push(`- **Rule 1 — Grade by weekday:** Mon=Pre-KG · Tue=KG · Wed=Class 1 · Thu=Class 2 · Fri=Class 3 · Sat=Class 4 · Sun=Class 5`);
lines.push(`- **Rule 2 — Theme rotation:** alphabetical pool per grade, advance by 1 each week, skip if yesterday used the same theme`);
lines.push(`- **Rule 3 — Activity preset:** Mon–Sat = \`mix\` · Sun = \`colouring\``);
lines.push(`- **Rule 4 — Indexable curated slug:** each row maps to a real worksheet slug that will be added to the sitemap as it's posted`);
lines.push(``);
lines.push(`## The 28 days`);
lines.push(``);
lines.push(`| #  | Day      | Date     | Grade    | Theme        | Activity   | Headline                              |`);
lines.push(`|---:|----------|----------|----------|--------------|------------|---------------------------------------|`);
rows.forEach(r => {
  lines.push(`| ${String(r.n).padStart(2)} | ${r.dayName.padEnd(8)} | ${r.dateStr.padEnd(8)} | ${r.grade.padEnd(8)} | ${r.theme.padEnd(12)} | ${r.activity.padEnd(10)} | ${r.headline.padEnd(37)} |`);
});

lines.push(``);
lines.push(`## Coverage check (sanity)`);
const themeCount = {};
const gradeCount = {};
rows.forEach(r=>{
  themeCount[r.themeKey] = (themeCount[r.themeKey]||0) + 1;
  gradeCount[r.gradeKey] = (gradeCount[r.gradeKey]||0) + 1;
});
lines.push(``);
lines.push(`**Per grade (should be 4 each — one pin per week × 4 weeks):**`);
lines.push(``);
Object.entries(gradeCount).forEach(([g,c])=>lines.push(`- ${GRADE_LABEL[g]}: ${c}`));
lines.push(``);
lines.push(`**Per theme (variety check):**`);
lines.push(``);
Object.entries(themeCount).sort((a,b)=>b[1]-a[1]).forEach(([t,c])=>lines.push(`- ${THEME_LABEL[t]||t}: ${c}`));

lines.push(``);
lines.push(`## How to edit this`);
lines.push(``);
lines.push(`Two cheap ways to react:`);
lines.push(`1. **Tell me in chat** — "Day 7 should be Animals not Fruits" → I edit + regenerate`);
lines.push(`2. **Edit a rule** in \`tools/gen-pinterest-schedule.js\` and re-run \`node tools/gen-pinterest-schedule.js\``);
lines.push(``);
lines.push(`The schedule is **not yet wired to the bot** — this is a review artifact only. The bot wakes up daily, reads row N (where N = days since 13 Jun), generates that exact worksheet, and posts it.`);

const out = path.join(__dirname, '..', '..', 'Resources', 'pinterest-28-day-schedule.md');
fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out, lines.join('\n'));
console.log(`✓ Wrote ${out}`);
console.log(`  ${rows.length} rows · grades: ${Object.keys(gradeCount).length} · themes: ${Object.keys(themeCount).length}`);
